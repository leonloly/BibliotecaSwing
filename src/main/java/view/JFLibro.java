    private void jTableLibroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableLibroMouseClicked
        SimpleDateFormat format = new SimpleDateFormat("yyyy/MM/dd");
        lib = dataList.get(jTableLibro.getSelectedRow());

        String fecha = format.format(lib.getPublicado());

        jtTitulo.setText(lib.getTitulo());
        jtEtiqueta.setText(lib.getEtiqueta());
        jtPublicado.setText(fecha);
        jtEdicion.setText(String.valueOf(lib.getEdicion().toString()));
        jtPrecio.setText(String.valueOf(lib.getPrecioRenta()));
        jtIsbn.setText(lib.getIsbn());

        edit = true;
    }//GEN-LAST:event_jTableLibroMouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (jtTitulo.getText().isEmpty()
                || jtEtiqueta.getText().isEmpty()
                || jtPublicado.getText().isEmpty()
                || jtEdicion.getText().isEmpty()
                || jtPrecio.getText().isEmpty()
                || jtIsbn.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No debe dejar campos vacios");
            return;
        }
        if (!edit) {
            lib = new Libros();
        }
        lib.setTitulo(jtTitulo.getText());
        lib.setEtiqueta(jtEtiqueta.getText());
        lib.setPublicado(new Date(jtPublicado.getText()));
        lib.setEdicion(Short.valueOf(jtEdicion.getText()));
        lib.setPrecioRenta(new BigDecimal(jtPrecio.getText()));
        lib.setIsbn(jtIsbn.getText());

        if (tipoList.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccionar ");
        } else {
            Autores au = new Autores();
            LibroTipos lt = new LibroTipos();
            LibroClasificaciones lc = new LibroClasificaciones();

            Object[] rowAut = autList.get(0);
            Object[] rowTip = tipoList.get(0);
            Object[] rowCla = clasList.get(0);

            au.setCodigo(Integer.parseInt(rowAut[0].toString()));
            lib.setCodigoAutor(au);

            lt.setCodigo(Integer.parseInt(rowTip[0].toString()));
            lib.setCodigoTipo(lt);

            lc.setCodigo(Integer.parseInt(rowCla[0].toString()));
            lib.setCodigoClasificacion(lc);

            if (lib.save()) {
                JOptionPane.showMessageDialog(this, (edit ? "Editado" : "Creado") + " correctamente");
                jtTitulo.setText("");
                jtEtiqueta.setText("");
                jtPublicado.setText("");
                jtEdicion.setText("");
                jtPrecio.setText("");
                jtIsbn.setText("");
                edit = false;
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar ");
                return;
            }
            showData();
        }
        showData();
    }//GEN-LAST:event_jButton1MouseClicked

