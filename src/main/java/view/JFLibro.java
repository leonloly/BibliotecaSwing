    void showData() {
        DefaultTableModel model = (DefaultTableModel) jTableLibro.getModel();
        dataList = (new Libros()).List();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }

        for (Libros s : dataList) {
            Object[] row = {s.getCodigo(), s.getTitulo(), s.getEtiqueta(), s.getPublicado(), s.getEdicion(), s.getPrecioRenta(), s.getIsbn(), s.getCodigoTipo().getNombre(), s.getCodigoAutor().getNombre(), s.getCodigoClasificacion().getNombre()};
            model.addRow(row);
        }
    }
