    void showData() {
        DefaultTableModel model = (DefaultTableModel) jTableUsuario.getModel();
        dataList = (new Usuarios()).list();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }

        for (Usuarios s : dataList) {
            Object[] row = {s.getCodigo(), s.getNombre(), s.getApellido(), s.getCarnet(), s.getCodigoPerfil().getNombre()};
            model.addRow(row);
        }
    }
