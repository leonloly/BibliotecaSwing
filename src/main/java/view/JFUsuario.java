    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (jtApellido.getText().isEmpty() || jtApellido.getText().isEmpty() || jtCarnet.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No debe dejar campos vacios");
            return;
        }
        if (!edit) {
            us = new Usuarios();
        }
        us.setNombre(jtNombre.getText());
        us.setApellido(jtApellido.getText());
        us.setCarnet(jtCarnet.getText());

        if (perfilUsuarioList.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccionar un Perfil de usuario");
        } else {

            UsuarioPerfiles p = new UsuarioPerfiles();
            Object[] row = perfilUsuarioList.get(0);
            p.setCodigo(Integer.parseInt(row[0].toString()));
            us.setCodigoPerfil(p);

            if (us.save()) {
                JOptionPane.showMessageDialog(this, (edit ? "Editado" : "Creado") + " correctamente");
                jtNombre.setText("");
                jtApellido.setText("");
                jtCarnet.setText("");
                edit = false;
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar Usuario");
                return;
            }
            showData();
        }
    }//GEN-LAST:event_jButton1MouseClicked

